  isSubmittingComment.value = true;

  setTimeout(() => {
    const newId = Math.max(...comments.value.map(c => c.id), 0) + 1;
    const comment: Comment = {
      id: newId,
      name: newComment.value.name.trim(),
      email: newComment.value.email || undefined,
