  name: "",
  company: "",
  email: "",
  budget: "",
  message: "",
})

const isSending = ref(false)
const isSubmitted = ref(false)

function clearErrors() {
  formErrors.name = ""
  formErrors.company = ""
  formErrors.email = ""
  formErrors.budget = ""
  formErrors.message = ""
}

function validateAdvertiserForm() {
  clearErrors()

  let valid = true
  const emailPattern = /^[^\s@]+@[^\s@]+\.[^\s@]+$/

  if (!advertiserForm.name.trim()) {
    formErrors.name = "бһҹбһјбһҳбһ”бһүбҹ’бһҮбһ¶бһҖбҹӢбһҲбҹ’бһҳбҹ„бҹҮбһҡбһ”бһҹбҹӢбһўбҹ’бһ“бһҖбҹ”"
    valid = false
  }

  if (!advertiserForm.company.trim()) {
    formErrors.company = "бһҹбһјбһҳбһ”бһүбҹ’бһҮбһ¶бһҖбҹӢбһҲбҹ’бһҳбҹ„бҹҮбһҖбҹ’бһҡбһ»бһҳбһ бҹҠбһ»бһ“бҹ”"
    valid = false
  }

  if (!advertiserForm.email.trim()) {
    formErrors.email = "бһҹбһјбһҳбһ”бһүбҹ’бһҮбһ¶бһҖбҹӢбһўбҹҠбһёбһҳбҹӮбһӣбҹ”"
    valid = false
  } else if (!emailPattern.test(advertiserForm.email)) {
    formErrors.email = "бһҹбһјбһҳбһ”бһүбҹ’бһ…бһјбһӣбһўбһ¶бһҹбһҷбһҠбҹ’бһӢбһ¶бһ“бһўбҹҠбһёбһҳбҹӮбһӣбһҸбҹ’бһҡбһ№бһҳбһҸбҹ’бһҡбһјбһңбҹ”"
    valid = false
  }

  if (!advertiserForm.budget.trim()) {
    formErrors.budget = "бһҹбһјбһҳбһҮбҹ’бһҡбһҫбһҹбһҡбһҫбһҹбһ…бһ“бҹ’бһӣбҹ„бҹҮбһҖбһүбҹ’бһ…бһ”бҹӢбһҗбһңбһ·бһҖбһ¶бҹ”"
    valid = false
  }

  if (!advertiserForm.message.trim()) {
    formErrors.message = "бһҹбһјбһҳбһ”бҹ’бһҡбһ¶бһ”бҹӢбһҷбһҫбһ„бһ”бһ“бҹ’бһҸбһ·бһ…бһ”бһ“бҹ’бһҸбһҪбһ…бһўбҹҶбһ–бһёбһҷбһ»бһ‘бҹ’бһ’бһ“бһ¶бһҖбһ¶бһҡбһҡбһ”бһҹбҹӢбһўбҹ’бһ“бһҖбҹ”"
    valid = false
  } else if (advertiserForm.message.trim().length < 20) {
    formErrors.message = "бһҹбһјбһҳбһҹбһҡбһҹбҹҒбһҡбһҷбҹүбһ¶бһ„бһ бҹ„бһ…бһҺбһ¶бһҹбҹӢбһұбҹ’бһҷбһ”бһ¶бһ“ бҹўбҹ  бһҸбһҪбһўбһҖбҹ’бһҹбһҡбҹ”"
    valid = false
  }

  return valid
}

function submitAdvertiserForm() {
  if (!validateAdvertiserForm()) return

  isSending.value = true
  const mailto = new URL("mailto:ads@cobo-news.com")
  mailto.searchParams.set("subject", `[Advertising Inquiry] ${advertiserForm.company}`)
  mailto.searchParams.set(
    "body",
    `Name: ${advertiserForm.name}\nCompany: ${advertiserForm.company}\nEmail: ${advertiserForm.email}\nBudget: ${advertiserForm.budget}\n\n${advertiserForm.message}`,
  )

  window.location.href = mailto.toString()

  isSending.value = false
  isSubmitted.value = true

  advertiserForm.name = ""
  advertiserForm.company = ""
  advertiserForm.email = ""
  advertiserForm.budget = ""
  advertiserForm.message = ""

  setTimeout(() => {
    isSubmitted.value = false
  }, 4000)
}
